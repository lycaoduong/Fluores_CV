Mat ImageProcess::gray_level(int row, int col)
{
	Mat grayVolume(row, col, CV_8UC1, cv::Scalar(0));
	for (int x = 0; x < grayVolume.rows; x++)
	{
		for (int y = 0; y < grayVolume.cols; y++)
		{
			if (y > 60)
			{
				grayVolume.at<uchar>(x, y) = 255 - x;
			}
			else
			{
				grayVolume.at<uchar>(x, y) = 255;
			}
		}
	}
	return grayVolume;
}

Mat ImageProcess::scale_level(Mat im_gray, int min, int max)
{
	threshold(im_gray, im_gray, min, 255, THRESH_TOZERO);
	threshold(im_gray, im_gray, max, 255, THRESH_TOZERO_INV);
	float scale = (float(max) - float(min)) / 255;
	for (int j = 0; j<im_gray.rows; j++)
	{
		for (int i = 0; i<im_gray.cols; i++)
		{
			if (im_gray.at<uchar>(j, i) > min)
			{
				im_gray.at<uchar>(j, i) = int((float(im_gray.at<uchar>(j, i)) - float(min)) / scale); 
			}
			else
			{
				im_gray.at<uchar>(j, i) = 0;
			}
		}
	}
	return im_gray;
}

